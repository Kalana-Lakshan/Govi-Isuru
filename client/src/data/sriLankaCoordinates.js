// Get coordinates for a GN Division
export const getGNCoordinates = (district, gnDivision) => {
  const districtData = districtCoordinates[district];
  if (!districtData) return null;
  
  // First check direct GN match
  if (districtData.gnDivisions[gnDivision]) {
    return districtData.gnDivisions[gnDivision];
  }
  
  // If not found, return district center
  return districtData.center;
};

// Get all GN coordinates for a district
export const getDistrictGNs = (district) => {
  const districtData = districtCoordinates[district];
  if (!districtData) return [];
  
  return Object.entries(districtData.gnDivisions).map(([name, coords]) => ({
    name,
    ...coords
  }));
};

// Get district center
export const getDistrictCenter = (district) => {
  const districtData = districtCoordinates[district];
  return districtData ? districtData.center : { lat: 7.8731, lng: 80.7718 }; // Sri Lanka center
};

